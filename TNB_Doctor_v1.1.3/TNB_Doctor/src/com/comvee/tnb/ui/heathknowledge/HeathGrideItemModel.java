package com.comvee.tnb.ui.heathknowledge;

public class HeathGrideItemModel {
	private String hotType;
	private String hotTypeUrl;
	private String url;
	private float width;
	private float height;
	private String title;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public float getWidth() {
		return width;
	}

	public void setWidth(float width) {
		this.width = width;
	}

	public float getHeight() {
		return height;
	}

	public void setHeight(float height) {
		this.height = height;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getHotType() {
		return hotType;
	}

	public void setHotType(String hotType) {
		this.hotType = hotType;
	}

	public String getHotTypeUrl() {
		return hotTypeUrl;
	}

	public void setHotTypeUrl(String hotTypeUrl) {
		this.hotTypeUrl = hotTypeUrl;
	}

}
