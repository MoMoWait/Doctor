package com.comvee.tnb.model;

public class IndexGridModel {
	private int ImageLocUrl;
	private String name;
	private double width;

	public double getWidth() {
		return width;
	}

	public void setWidth(double width) {
		this.width = width;
	}

	public int getImageLocUrl() {
		return ImageLocUrl;
	}

	public void setImageLocUrl(int imageLocUrl) {
		ImageLocUrl = imageLocUrl;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
