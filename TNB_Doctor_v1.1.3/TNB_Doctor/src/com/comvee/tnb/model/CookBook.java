package com.comvee.tnb.model;

public class CookBook {
	private int carbohydrates;
	private String cookBookMakeSteps;
	private int cookbookId;
	private int fat;
	private int heat;
	private String imgUrl;
	private String ingredientsDetail;
	private String insertDate;
	private String nutritionistsRecommend;
	private int protein;
	private String pushDate;
	private int sugarBenefits;
	private String cookbookName;

	public int getCarbohydrates() {
		return carbohydrates;
	}

	public void setCarbohydrates(int carbohydrates) {
		this.carbohydrates = carbohydrates;
	}

	public String getCookBookMakeSteps() {
		return cookBookMakeSteps;
	}

	public void setCookBookMakeSteps(String cookBookMakeSteps) {
		this.cookBookMakeSteps = cookBookMakeSteps;
	}

	public int getCookbookId() {
		return cookbookId;
	}

	public void setCookbookId(int cookbookId) {
		this.cookbookId = cookbookId;
	}

	public int getFat() {
		return fat;
	}

	public void setFat(int fat) {
		this.fat = fat;
	}

	public int getHeat() {
		return heat;
	}

	public void setHeat(int heat) {
		this.heat = heat;
	}

	public String getImgUrl() {
		return imgUrl;
	}

	public void setImgUrl(String imgUrl) {
		this.imgUrl = imgUrl;
	}

	public String getIngredientsDetail() {
		return ingredientsDetail;
	}

	public void setIngredientsDetail(String ingredientsDetail) {
		this.ingredientsDetail = ingredientsDetail;
	}

	public String getInsertDate() {
		return insertDate;
	}

	public void setInsertDate(String insertDate) {
		this.insertDate = insertDate;
	}

	public String getNutritionistsRecommend() {
		return nutritionistsRecommend;
	}

	public void setNutritionistsRecommend(String nutritionistsRecommend) {
		this.nutritionistsRecommend = nutritionistsRecommend;
	}

	public int getProtein() {
		return protein;
	}

	public void setProtein(int protein) {
		this.protein = protein;
	}

	public String getPushDate() {
		return pushDate;
	}

	public void setPushDate(String pushDate) {
		this.pushDate = pushDate;
	}

	public int getSugarBenefits() {
		return sugarBenefits;
	}

	public void setSugarBenefits(int sugarBenefits) {
		this.sugarBenefits = sugarBenefits;
	}

	public String getCookbookName() {
		return cookbookName;
	}

	public void setCookbookName(String cookbookName) {
		this.cookbookName = cookbookName;
	}

}
