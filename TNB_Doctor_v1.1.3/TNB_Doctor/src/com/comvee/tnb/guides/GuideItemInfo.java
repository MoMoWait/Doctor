package com.comvee.tnb.guides;

public class GuideItemInfo {

	private String defaultValue;
	private int hasGoto;
	private int ifFloat;
	private String itemID;
	private int itemSeq;
	private String itemTitle;
	private String itemUnit;
	private String itemValue;
	private int maxValue;
	private int minValue;

	public String getDefaultValue() {
		return defaultValue;
	}

	public void setDefaultValue(String defaultValue) {
		this.defaultValue = defaultValue;
	}

	public int getHasGoto() {
		return hasGoto;
	}

	public void setHasGoto(int hasGoto) {
		this.hasGoto = hasGoto;
	}

	public int getIfFloat() {
		return ifFloat;
	}

	public void setIfFloat(int ifFloat) {
		this.ifFloat = ifFloat;
	}

	public String getItemID() {
		return itemID;
	}

	public void setItemID(String itemID) {
		this.itemID = itemID;
	}

	public int getItemSeq() {
		return itemSeq;
	}

	public void setItemSeq(int itemSeq) {
		this.itemSeq = itemSeq;
	}

	public String getItemTitle() {
		return itemTitle;
	}

	public void setItemTitle(String itemTitle) {
		this.itemTitle = itemTitle;
	}

	public String getItemUnit() {
		return itemUnit;
	}

	public void setItemUnit(String itemUnit) {
		this.itemUnit = itemUnit;
	}

	public String getItemValue() {
		return itemValue;
	}

	public void setItemValue(String itemValue) {
		this.itemValue = itemValue;
	}

	public int getMaxValue() {
		return maxValue;
	}

	public void setMaxValue(int maxValue) {
		this.maxValue = maxValue;
	}

	public int getMinValue() {
		return minValue;
	}

	public void setMinValue(int minValue) {
		this.minValue = minValue;
	}

}
